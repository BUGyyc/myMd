    public int KthSmallest (TreeNode root, int k) {
        if (root == null) {
            return 0;
        }
        Stack<TreeNode> stack = new Stack<TreeNode> ();
        while (true) {
            while (root != null) {
                stack.Push (root);
                root = root.left;
            }
            root = stack.Pop ();
            k--;
            if (k == 0) {
                return root.val;
            }
            root = root.right;
        }
        return 0;
    }
}
